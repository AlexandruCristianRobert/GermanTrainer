// src/data/daSubstitution.ts
//
// Authored dataset for the Da-compound SUBSTITUTION drill (gap-fill). Each item
// pairs a natural context sentence that USES a prepositional collocation with an
// anaphoric follow-up (the stem) in which the prepositional phrase is replaced by
// a da-compound — the gap the learner fills. Every object of the preposition is a
// THING or an ABSTRACT, never a person or animal: da-compounds only stand in for
// non-persons (see CONTEXT.md → "Da-compound"), so a person object would teach
// wrong German. Accuracy here is a shipping gate.
//
// Items JOIN the curated collocation seed by `collocationId`; the answer is derived
// (never stored) from the joined collocation's preposition via daCompound(). The
// invariants in tests/data/daSubstitution.test.ts enforce the join, the level, the
// single gap, and that the stem never leaks the answer.

import { COLLOCATIONS, type CollocationLevel } from './collocations'
import { daCompound } from './daCompounds'

export interface SubstitutionItem {
  /** Unique id, `sub-<collocationId>`. */
  id: string
  /** Joins COLLOCATIONS by id; supplies preposition, case and level. */
  collocationId: string
  /** Natural sentence USING the collocation with a thing/abstract object. */
  context: string
  /** Anaphoric follow-up with exactly one `___` where the da-compound goes. */
  stem: string
  /** Copied from the joined collocation. */
  level: CollocationLevel
}

/**
 * Near-neighbour prepositions for each primary collocation preposition — three
 * distinct, compoundable distractors (never the key itself) used to build the
 * multiple-choice foils for the substitution drill. Covers every preposition
 * that appears in COLLOCATIONS.
 */
export const NEIGHBOR_PREPS: Record<string, string[]> = {
  an: ['auf', 'über', 'von'], auf: ['an', 'über', 'um'], über: ['an', 'von', 'auf'],
  von: ['über', 'an', 'nach'], für: ['um', 'gegen', 'zu'], um: ['für', 'auf', 'über'],
  mit: ['zu', 'bei', 'von'], nach: ['zu', 'um', 'von'], vor: ['von', 'gegen', 'um'],
  zu: ['für', 'nach', 'mit'], gegen: ['für', 'um', 'vor'], bei: ['mit', 'zu', 'an'],
  aus: ['von', 'in', 'nach'], in: ['an', 'auf', 'aus'], unter: ['über', 'von', 'mit'],
}

const byId = new Map(COLLOCATIONS.map(c => [c.id, c]))

/** The correct answer: the da-compound of the joined collocation's preposition. */
export function substitutionAnswer(item: SubstitutionItem): string {
  const collocation = byId.get(item.collocationId)
  if (!collocation) throw new Error(`Unknown collocationId: ${item.collocationId}`)
  return daCompound(collocation.preposition)
}

